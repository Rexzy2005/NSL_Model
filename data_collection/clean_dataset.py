"""
clean_dataset.py
────────────────
Reset every piece of generated data so you can start collecting
proper data from scratch.  Designed to be SAFE to run — it only
deletes files in known locations and refuses to touch anything else.

What it removes:
  dataset/downloads/                — every downloaded .mp4
  dataset/raw/                      — every extracted .npy
  dataset/processed/                — X.npy, y.npy, label_map.json
  dataset/splits/                   — every train/val/test .npy
  dataset/.mp_models/               — downloaded MediaPipe models
  dataset/.extracted_done.json      — extraction resume fingerprint
  dataset/annotations.json          — all saved annotations
  dataset/.last_download.json       — any downloader state

What it KEEPS:
  dataset/ folder itself
  All scripts in data_collection/
  All scripts in training/, evaluation/, flutter_app/, docs/
  README.md, requirements.txt, .gitignore

Usage:
  python data_collection/clean_dataset.py
"""

import os
import sys
import shutil
import pathlib
import logging

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s  %(levelname)-7s  %(message)s",
    datefmt="%H:%M:%S",
)
log = logging.getLogger(__name__)

# ── Paths that will be removed ────────────────────────────────────────────────
DATASET_DIR = pathlib.Path("dataset")

# Files / dirs that are clearly generated by our pipeline
PIPELINE_PATHS = [
    DATASET_DIR / "downloads",
    DATASET_DIR / "raw",
    DATASET_DIR / "processed",
    DATASET_DIR / "splits",
    DATASET_DIR / ".mp_models",
    DATASET_DIR / ".extracted_done.json",
    DATASET_DIR / "annotations.json",
    DATASET_DIR / ".last_download.json",
]


def main() -> None:
    if not DATASET_DIR.exists():
        log.error("dataset/ directory not found — nothing to clean.")
        sys.exit(1)

    # ── Safety check: refuse to run if dataset/ contains files we don't know ─
    allowed_files = {
        "downloads", "raw", "processed", "splits",
        ".mp_models", ".extracted_done.json", "annotations.json",
        ".last_download.json",
    }
    unexpected = [
        p.name for p in DATASET_DIR.iterdir() if p.name not in allowed_files
    ]
    if unexpected:
        log.error(
            "Refusing to clean — found unexpected items in dataset/: %s",
            unexpected,
        )
        log.error(
            "If you really want to clean, move those files out first, "
            "or edit PIPELINE_PATHS in this script."
        )
        sys.exit(1)

    # ── Show what will be removed and confirm ────────────────────────────────
    print()
    log.info("The following will be DELETED:")
    for p in PIPELINE_PATHS:
        if p.exists():
            kind = "DIR " if p.is_dir() else "FILE"
            size = _format_size(_size_of(p))
            log.info("  %s  %-40s  %s", kind, str(p), size)
        else:
            log.info("  ---  %-40s  (not present, skipped)", str(p))

    print()
    answer = input(
        "Type 'yes' to confirm deletion (anything else aborts): "
    ).strip().lower()
    if answer != "yes":
        log.warning("Aborted by user. Nothing was deleted.")
        return

    # ── Delete ───────────────────────────────────────────────────────────────
    deleted_files = 0
    deleted_dirs  = 0
    for p in PIPELINE_PATHS:
        if not p.exists():
            continue
        try:
            if p.is_dir():
                # remove all contents first, then the dir itself
                for child in p.rglob("*"):
                    if child.is_file():
                        child.unlink()
                        deleted_files += 1
                    elif child.is_symlink() or child.is_dir():
                        shutil.rmtree(child, ignore_errors=True)
                if p.exists():
                    shutil.rmtree(p, ignore_errors=True)
                    deleted_dirs += 1
            else:
                p.unlink()
                deleted_files += 1
        except Exception as exc:
            log.error("Could not delete %s: %s", p, exc)

    # ── Recreate empty target directories so the pipeline is ready to go ─────
    (DATASET_DIR / "downloads").mkdir(exist_ok=True)

    print()
    log.info("─" * 60)
    log.info("Clean complete.")
    log.info("  Files deleted : %d", deleted_files)
    log.info("  Dirs  deleted : %d", deleted_dirs)
    log.info("─" * 60)
    log.info("Dataset reset. Next steps:")
    log.info("  1. python data_collection/downloader.py      (download videos)")
    log.info("  2. python data_collection/annotator.py       (annotate clips)")
    log.info("  3. python data_collection/extract_landmarks.py")
    log.info("  4. python data_collection/verify_dataset.py")
    log.info("  5. python data_collection/augment.py")
    log.info("  6. python data_collection/build_dataset.py")


# ── Helpers ───────────────────────────────────────────────────────────────────
def _size_of(path: pathlib.Path) -> int:
    """Return total size in bytes for a file or directory tree."""
    if path.is_file():
        return path.stat().st_size
    return sum(child.stat().st_size for child in path.rglob("*")
               if child.is_file())


def _format_size(n_bytes: int) -> str:
    for unit in ("B", "KB", "MB", "GB"):
        if n_bytes < 1024:
            return f"{n_bytes:.1f} {unit}"
        n_bytes /= 1024
    return f"{n_bytes:.1f} TB"


# ── Entry point ───────────────────────────────────────────────────────────────
if __name__ == "__main__":
    main()